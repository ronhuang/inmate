import re, htmlentitydefs
from datetime import tzinfo, timedelta


class SGT(tzinfo):
    "Time zone for Singapore"

    def utcoffset(self, dt):
        return timedelta(hours=8)

    def dst(self, dt):
        return timedelta(0)

    def tzname(self, dt):
        return "Asia/Singapore"


